<h2>Interaktivní testy</h2>

<p>Často opomíjeným typem úloh jsou uzavřené testové úlohy. V nich je řešiteli nabídnuto několik alternativ odpovědí, přičemž správných může být i více. Při vhodné formulaci úlohy je tak možné velmi rychle a účinně ověřit mírů pochopení daného učiva. Způsoby hodnocení takových úloh se různí (viz www-stránky v sekci <a href="index.php?web=odkazy">Odkazy</a>). Zde se přikláníme ke způsobu  hodnocení “všechno, nebo nic”, kdy je úloha považována za správně vyřešenou (plný bodový zisk), označí-li řešitel právě správné odpovědi; v opačném případě je úloha považována za nesprávně vyřešenou (žádný bodový zisk). Všechny uvedené testové úlohy jsou autorské. Pro lepší orientaci jsou rozčleněny do jednotlivých tematických celků, přičemž úlohy v souboru daného tematického celku mohou vyžadovat i znalosti celků předchozích. (Srovnávací studijní literaturou pokrývající jednotlivé tematické celky je učebnice  Halliday, D., Resnick, R., Walker, J.: Fyzika. Prometheus, Praha 2000 (překlad z angličtiny).)</p>

<h3>Návod k práci s testovými úlohami:</h3>

<p> Zvolte tematický celek a v nabídnuté úloze označte alternativy
odpovědí podle instrukce v zadání. Během řešení úlohy běží čas, který
je pro ni přidělen, je zde tedy možnost průběžné kontroly časové
rezervy (v řešení úlohy lze však pokračovat i po uplynutí časového
limitu). Provedením požadavku <b>Vyhodnotit úlohu</b> zjistíte, zda
jste úlohu vyřešili správně a proč, případně v čem jste chybovali a
proč, a které z textů na těchto stránkách doporučujeme prostudovat,
pokud si řešením nejste zcela jisti. </p>




<h3>Důležité upozornění:</h3>

<ul>
<li>Věnujte pozornost formulaci zadání – často bývá požadavkem vybrat ta tvrzení, která jsou <b>obecně</b> (tj. bez doplňujících předpokladů) správná/nesprávná.</li>
<li>Odpověď, která obsahuje správné tvrzení, ale nesprávnou argumentaci, je <b>nesprávná</b>.</li>
<li>… a na závěr – nenechte se odradit. Několik prvních testových úloh se vám může zdát poněkud obtížných, ale vytrvejte - vše je jen věcí cviku a úspěch se jistě dostaví!</li>
</ul>


