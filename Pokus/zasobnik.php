<h2>Zásobník úloh</h2>

<p>Nezbytným krokem k aktivnímu pochopení a upevnění fyzikálních poznatků je řešení úloh. Zde předložený Zásobník úloh sestává z více než tří desítek otevřených úloh (tj. úloh, v nichž řešitel sám tvoří odpověď) z klasické mechaniky a molekulové fyziky. Doplňuje tak množiny úloh k samostatnému řešení uváděné vždy v závěru každé z <a href="index.php?web=nastrahy">Nástrah fyziky</a>, které nemohly být s ohledem na rozsah textů příliš mohutné, vyhýbá se však “klonování” úloh, kdy jeden typ úlohy zařazené v několika  nepatrných obměnách ověřuje tytéž poznatky. Ve snaze vést studenty k samostatné práci, posilovat důvěru ve vlastní schopnosti a rozvíjet potřebu konzultovat s kolegy i pedagogy zde nejsou uvedeny výsledky úloh.</p>

<p>Poznámka: U úloh, které byly převzaty či inspirovány literaturou je uveden odkaz na příslušný pramen. Seznam literatury je stejný jako pro <a href="index.php?web=nastrahy">Nástrahy fyziky</a>.</p>

