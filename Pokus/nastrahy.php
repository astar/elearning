<h2>Nástrahy fyziky</h2>

<p>Učební texty se souhrnným názvem Nástrahy fyziky upozorňují na tradičně problematická místa jednotlivých tematických celků klasické mechaniky a molekulové fyziky. S výjimkou Hlavního textu, který tematiku stručně uvozuje, mají charakter podrobně komentovaných  řešených příkladů, v nichž jsou  postupně odkrývána a diskutována místa, ve kterých se často chybuje. Samozřejmostí je přehledné shrnutí nejdůležitějších poznatků získaných v průběhu řešení příkladů a zařazení několika námětů k  přemýšlení i úloh k samostatnému řešení. Seznam běžně dostupné literatury, z níž se při přípavě textů vycházelo, je uveden v samostatném souboru.</p>

