<h2>Úvod</h2>

<p>Vítejte na stránkách Kurzu klasické mechaniky v úlohách a testech
pro e-learning, které vznikly v rámci projektu <a
href="http://www.frvs.cz/">FRVŠ</a> 1592/2009 a 1123/2011.</p>

<h3>Komu jsou stránky určeny?</h3>

<p>Především studentům a učitelům úvodního univerzitního kurzu klasické mechaniky a molekulové fyziky (bakalářské studijní programy) a studentům a učitelům didaktických kurzů fyziky (magisterské studijní programy). Jako zdroj inspirace, námětů k přemýšlení či úloh k řešení mohou posloužit také středoškolským učitelům fyziky a jejich studentům se zájmem o fyziku.</p>

<h3>Co na stránkách naleznete?</h3>

<ul>
<li>Informace <a href="index.php?web=projekt"><b>O projektu</b></a> spolu s kontakty na řešitelku, spoluřešitelku a spolupracovníky.</li>
<li><a href="index.php?web=nastrahy"><b>Nástrahy fyziky</b></a> (deset krátkých relativně nezávislých učebních textů zaměřených na problematická místa klasické mechaniky <br>a molekulové fyziky uvozených vstupním přehledem tematiky).</li>
<li><a href="index.php?web=zasobnik"><b>Zásobník úloh</b></a> (soubor více než tří desítek úloh k samostatnému řešení pokrývající tradiční i problematická místa jednotlivých tematických celků klasické mechaniky a molekulové fyziky).</li>
<li><a href="index.php?web=testy"><b>Interaktivní testy</b></a> (soubor sta testových úloh ze všech tematických celků klasické mechaniky a molekulové fyziky s  jednou i více správnými odpověďmi spolu s možností okamžitého vyhodnocení každé testové úlohy a detailní zpětnou vazbou).</li>
<li><a href="index.php?web=experimenty"><b>Demonstrační experimenty</b></a> (videozáznamy více i méně tradičních demonstračních experimentů spolu s výkladem či jednoduchými kontrolními úkoly).</li>
<li><a href="index.php?web=odkazy"><b>Odkazy</b></a> na vybrané internetové stránky související <br>s problematikou projektu.</li>
</ul>
